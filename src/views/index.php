			<h2 id="1">Udało ci się zrobić ostatnio zdjęcie jakiejś planety, komety bądź zjawiska i nie masz się z kim tym podzielić?</h2>
			<p>Ta strona jest właśnie dla Ciebie!</p>
			<h2>Jak mam się podzielić swoim dziełem i co z tego będę miał?</h2>
			<p>Twoje zdjęcie będzie brało udział w konkursie na top 5 najlepszych fotografii roku. By go przesłać przejdź do zakładki prześlij, lub kliknij <a href="przeslij">tutaj</a>.</p>
			<h2>Jak mogę podejrzeć zdjęcia z poprzednich konkursów?</h2>
			<p>Wchodząc w zakładkę galeria, wybierz interesujący Cię rok i podziwiaj!</p>
			<h2>Czy astrofotografia może być hobby?</h2>
			<p>Oczywiście, że tak. Wielu ludzi zajmuje się astrofotografią zawodowo, a próg wejścia jest bardzo niski. Wystarczy przecież telefon i bezchmurne niebo by zacząć.</p>
			<h2>No właśnie, co potrzebuję by móc robić czytelne i ładne zdjęcia kosmosu?</h2>
			<p>Smartfon na sam start oczywiście wystarczy, ale im chcemy robić ciekawsze zdjęcia planet lub satelit to sam smarfon nie pomoże. Będziemy musieli zastanowić się nad kupnem teleskopu, najlepiej wraz z aparatem.</p>
			<h2>Pewnie takie teleskopy są bardzo drogie?</h2>
			<p>Niekoniecznie, najtańsze teleskopy zaczynają się od 500 zł. Niepolecałbym ich natomiast, gdyż zależy nam na jakości, więc na teleskopach lepiej nie oszczędzać.</p>
			<h2>Gdzie najlepiej prowadzić obserwacje nieba?</h2>
			<p>Pewnie spotkałeś/aś się kiedyś z terminem <strong>zanieczyszczenie świetlne</strong>. W skrócie, w dużych miastach jest generowane tyle światła, poprzez np. światła drogowe, galerie handlowe czy biurowce, że nad naszymi głowami tworzy się łuna światła, która bardzo mocno utrudnia obserwację nieba. Więc nie trudno się domyślić, że trzeba wybierać miejsca słabo oświetlone z dala od skupisk ludzi i zabudowań. Nie bez powodu najlepsze zdjęcia Drogi Mlecznej w Polsce wychodzą w Bieszczadach.</p>
