				<h2 id="2">Astrofotografia - historia</h2>
				<figure class="w-tekscie-p">
					<img alt="Pierwsze zdjęcie zaćmienia Słońca z 1851 roku" src="static/images/zacmienie1851.jpg">
					<figcaption>
						Pierwsze zdjęcie zaćmienia Słońca wykonane przez <strong>Johann Julius Friedrich Berkowskiego</strong> 28 lipca 1851 r.<br>
						źródło: <a href="https://en.wikipedia.org/wiki/Astrophotography" target="_blank">wikipedia.org</a>
					</figcaption>
				</figure>
				<p>Pionierem astrofotografii był <strong>John William Draper</strong>, który wykonał fotografię Księżyca w 1840 roku. Następnie jego syn, Henry Draper, w 1880 r. jako pierwszy sfotografował obiekt pozasłoneczny – Wielką Mgławicę w Orionie. Korona Słońca zaś została po raz pierwszy sfotografowana podczas zaćmienia Słońca 28 lipca 1851 roku. <strong>Dr August Ludwig Busch</strong>, dyrektor Obserwatorium w ówczesnym Królewcu, wydał instrukcje lokalnemu fotografowi Johannowi Juliusowi Friedrichowi Berkowskiemu, aby wykonał <strong>zdjęcie zaćmienia</strong>. Sam Busch nie był obecny na miejscu, ale wolał obserwować zaćmienie z pobliskiego miasteczka. Zaraz po rozpoczęciu maksimum zaćmienia, Berkowski przez 84 sekund naświetlał w ognisku teleskopu płytkę dagerotypową i po wywołaniu obrazu uzyskano koronę Słońca.</p>
				<h2>A potem przyszedł wiek XX, i?</h2>
				<p>Teleskopy, teleskopy i jeszcze raz teleskopy. Zimna wojna między ZSRR a Stanami Zjednoczonymi popchnęła technologię dużo do przodu. Na początku XX wieku na całym świecie zbudowano teleskopy refrakcyjne i wyrafinowane duże teleskopy zwierciadlane zaprojektowane specjalnie do obrazowania fotograficznego, które przesuwały granice fotografii kosmicznej. W 1990 roku NASA wysłała na orbitę największy i najbardziej wszechstronny teleskop na świecie. Mowa tu oczywiście to <strong>Teleskopie Hubble’a</strong>. To dzięki niemu mamy piękne zdjęcia mgławic, czy galaktyk.</p>
