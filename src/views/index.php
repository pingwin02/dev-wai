				<h2>Mamy rok 2021, i co dalej?</h2>
				<figure class="w-tekscie-l">
					<img alt="Teleskop Hubbla" src="static/images/telhubbla.jpg">
					<figcaption>
						<strong>Teleskop Hubble’a</strong> widziany z pokładu promu kosmicznego Discovery.<br>
						źródło: <a href="https://pl.wikipedia.org/wiki/Kosmiczny_Teleskop_Hubble%E2%80%99a" target="_blank">wikipedia.org</a>
					</figcaption>
				</figure>
				<p>Oczywiście teleskopy będą rozwijane, jakość zdjęć stale będzie rosła, jak również nasze pojęcie o otaczającym nas kosmosie. NASA w niedalekiej przyszłości (18 grudnia 2021) planuje wysłać na orbitę <strong>teleskop Jamesa Webba</strong>, budowany w latach 2007–2021 teleskop kosmiczny do obserwacji w podczerwieni. Ma być następcą Kosmicznego Teleskopu Hubble’a. Wart prawie <strong>10 miliardów dolarów</strong> sprzęt będzie codziennie okrążać Ziemię w celu fotografowanie nieodkrytych jeszcze części tego nieznanego świata. Oprócz tego celami misji będą: obserwacje pierwszych gwiazd powstałych po Wielkim Wybuchu, badanie formowania się i ewolucji galaktyk, badanie tworzenia gwiazd i systemów planetarnych. Teleskop został nazwany na cześć nieżyjącego już Jamesa Edwina Webba, który był amerykańskim urzędnikiem rządowym, odpowiadał m.in. za realizację programu Apollo w NASA.</p>
